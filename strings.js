
let str1="Hello World";
console.log(str1);
let str2="9202053";
console.log(str2);

//length --> returns a length of the string
let lenstr1 = str1.length;
console.log("length of the string 1:",lenstr1);
let lenstr2 = str2.length;
console.log("length of the string 2:",lenstr2);

//charAt returns the character of the particular position
let charstr1 = str1.charAt(7);
console.log("charAt : ",charstr1);

let charstr2 = str2.charAt(3);
console.log("charAt : ",charstr2);

//charCodeAt() returns a unicode of the particular character


let charCodestr1=str1.charCodeAt(2);
console.log("charCodeAt : ",charCodestr1);

let charCodestr2=str2.charCodeAt(2);
console.log("charCodeAt : ",charCodestr2);

//toLowerCase
let lowerstr1 = str1.toLowerCase();
console.log("LowerCase:",lowerstr1);

//toUpperCase
let upperstr1 = str1.toUpperCase();
console.log("UpperCase:",upperstr1);

